function openRoomModal(roomType) {
    const modal = document.getElementById('roomModal');
    const modalTitle = document.getElementById('modalTitle');
    const modalGallery = document.getElementById('modalGallery');

    modalTitle.textContent = roomType.toUpperCase() + ' Oda Galerisi';

    modalGallery.innerHTML = '';

    roomImages[roomType].forEach(imageUrl => {
        const galleryItem = document.createElement('div');
        galleryItem.className = 'modal-gallery-item';
        galleryItem.innerHTML = `<img src="${imageUrl}" alt="${roomType} Room">`;
        modalGallery.appendChild(galleryItem);
    });

    modal.style.display = 'block';
    document.body.style.overflow = 'hidden';
}

document.addEventListener('DOMContentLoaded', function () {
    const modal = document.getElementById('roomModal');
    const closeBtn = document.querySelector('.close');

    closeBtn.addEventListener('click', function () {
        modal.style.display = 'none';
        document.body.style.overflow = 'auto';
    });

    modal.addEventListener('click', function (e) {
        if (e.target === modal) {
            modal.style.display = 'none';
            document.body.style.overflow = 'auto';
        }
    });

    document.addEventListener('keydown', function (e) {
        if (e.key === 'Escape' && modal.style.display === 'block') {
            modal.style.display = 'none';
            document.body.style.overflow = 'auto';
        }
    });
});

